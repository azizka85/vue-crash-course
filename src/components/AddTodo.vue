<template>
  <div>
    <form @submit.prevent="onSubmit">
      <input type="text" v-model="title">
      <button type="submit">Create</button>
    </form>
  </div>
</template>

<script>
export default {
  data() {
    return {
      title: ''
    }
  },
  methods: {
    onSubmit() {
      if(this.title.trim()) {
        const newTodo = {
          id: Date.now(),
          title: this.title,
          completed: false
        }

        this.$emit('add-todo', newTodo)
        this.title = ''
      }
    }
  }
}
</script>

<style scoped>
  div {
    text-align: center;
    margin: 1rem 0rem;
  }

  form {
    display: inline-block;    
  }

  input {
    width: 400px;
  }
</style>
